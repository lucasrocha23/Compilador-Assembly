    private void abrir_diretorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrir_diretorioActionPerformed
        String currentDir = System.getProperty("user.dir");
        File atual = new File(currentDir + File.separator + "c√≥digos");
        
        JFileChooser fileChooser = new JFileChooser(atual);
              
        int retorno = fileChooser.showOpenDialog(this);
        
        if(retorno == JFileChooser.APPROVE_OPTION){
            File select = fileChooser.getSelectedFile();
            P_name.setText(select.getName());
            dir = select.getAbsolutePath();
            dir = dir.substring(0, dir.lastIndexOf(File.separator));
        }
    }//GEN-LAST:event_abrir_diretorioActionPerformed
    
